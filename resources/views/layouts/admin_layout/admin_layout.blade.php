<script src="https://ajax.googleapis.com/ajax/libs/jquery/3.3.1/jquery.min.js"></script>
<script type="text/javascript">
    $(document).ready(function(){
        var maxField = 10; //Input fields increment limitation
        var addButton = $('.add_button'); //Add button selector
        var wrapper = $('.field_wrapper'); //Input field wrapper
        var fieldHTML = '<div><input type="number" width="150px" style="margin-top: 4px" name="price[]" placeholder="Product price" required value=""/>\n' +
            '                                    <input type="text" width="150px" name="size[]" placeholder="Product size" required value=""/>\n' +
            '                                    <input type="text" width="150px" name="sku[]" placeholder="Product sku" required value=""/>\n' +
            '                                    <input type="number" width="150px" name="stock[]" placeholder="Product stock" required value=""/>\n' +
            '                                   <a href="javascript:void(0);" style="margin-left: 5px" class="remove_button btn btn-sm btn-outline-danger">Remove</a></div>'; //New input field html
        var x = 1; //Initial field counter is 1

        //Once add button is clicked
        $(addButton).click(function(){
            //Check maximum number of input fields
            if(x < maxField){
                x++; //Increment field counter
                $(wrapper).append(fieldHTML); //Add field html
            }
        });

        //Once remove button is clicked
        $(wrapper).on('click', '.remove_button', function(e){
            e.preventDefault();
            $(this).parent('div').remove(); //Remove field html
            x--; //Decrement field counter
        });
    });
</script>
